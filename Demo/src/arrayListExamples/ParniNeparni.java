package arrayListExamples;

public class ParniNeparni {
	
	private Integer parni;
	private Integer neparni;
	
	
	public Integer getParni() {
		return parni;
	}
	public void setParni(Integer parni) {
		this.parni = parni;
	}
	public Integer getNeparni() {
		return neparni;
	}
	public void setNeparni(Integer neparni) {
		this.neparni = neparni;
	}
	
	
	public ParniNeparni(Integer parni, Integer neparni) {
		super();
		this.parni = parni;
		this.neparni = neparni;
	}
	public ParniNeparni() {
		super();
	}
	
	

}
